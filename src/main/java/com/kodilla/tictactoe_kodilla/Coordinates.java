package com.kodilla.tictactoe_kodilla;

public class Coordinates {

   private int row;
   private int column;

   public Coordinates(int row, int column) {
      this.row = row;
      this.column = column;
   }

   public int getRow() {
      return row;
   }

   public int getColumn() {
      return column;
   }
}
